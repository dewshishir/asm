 MOV AH,1
 INT 21H
 SUB AL,48
 MOV BL,AL

 MOV AH,2
 MOV DL,0DH
 INT 21H
 MOV DL,0AH
 INT 21H

 LEA DX,B
 MOV AH,9
 INT 21H

 MOV AH,1
 INT 21H
 SUB AL,48
 MOV BH,AL


 MOV AH,2
 MOV DL,0DH
 INT 21H
 MOV DL,0AH
 INT 21H
